# goal: compare lists, tuples, and sets

# list: ordered, changeable (mutable), allows duplicates
favorite_snacks_list = ["chips", "cookies", "chips", "fruit"]

# tuple: ordered, not changeable (immutable)
favorite_snacks_tuple = ("chips", "cookies", "chips", "fruit")

# set: unordered, unique items only (no duplicates)
favorite_snacks_set = {"chips", "cookies", "chips", "fruit"}

print("list:", favorite_snacks_list)
print("tuple:", favorite_snacks_tuple)
print("set:", favorite_snacks_set)

print("\nnotice how the set removed duplicate items.")
